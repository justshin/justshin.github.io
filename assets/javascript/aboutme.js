$(document).ready(function() {
  $(".korea").click(function(){
    $(".starterimage").hide();
    $(".studyimage").hide();
    $(".careimage").hide();
    $(".cookimage").hide();
    $(".sportimage").hide();
    $(".starter").hide();
    $(".study").hide();
    $(".care").hide();
    $(".cook").hide();
    $(".sport").hide();
    $(".aboutmeimage").fadeIn(900);
    $(".aboutme").fadeIn(900);
  });
});

